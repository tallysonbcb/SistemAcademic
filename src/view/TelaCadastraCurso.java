    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tblCurso.getModel();
        String sql = "";
        if (txtCodigo.getText().equals("")) {
            sql = "SELECT * FROM curso";
            ArrayList<Curso> curso = Curso.ListarCursos(sql);
                        
            if (curso == null) {
                return;
            }

            System.out.println();
            if (curso.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nenhum curso cadastrado");
                return;
            }

            // percorre lista e mostra todas os cursos
            for (Curso c : curso) {

                String dados[] = new String[2];
                dados[0] = String.valueOf(c.getCurs_iden());
                dados[1] = c.getCurs_descricao();
                dtm.addRow(dados);
            }
        } else {
             int codigo = Integer.parseInt(txtCodigo.getText());
             sql = "SELECT * FROM curso WHERE curs_iden = " + codigo; 
             ArrayList<Curso> curso = Curso.ListarCursos(sql);
            if (curso == null) {
                return;
            }

            System.out.println();
            if (curso.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nenhum curso cadastrado");
                return;
            }

            // percorre lista e mostra todas as pessoas           
            for (Curso c : curso) {

                txtCodigo.setText(String.valueOf(c.getCurs_iden()));
                txtDescricao.setText(c.getCurs_descricao());
                String coordenador = buscaCoordenador(c.getCurs_coord_iden());
                cmbCoordenador.removeAllItems();
                cmbCoordenador.addItem(coordenador);
  
        }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

