   public String buscaCoordenador(String nome){
       String sql = "SELECT * FROM pessoa WHERE pes_funcao = 'Coordenador'";
            ArrayList<Pessoa> pessoa = Pessoa.listarPessoas(sql);
            OrdenacaoBolha ob = new OrdenacaoBolha();
            ob.bolha(pessoa);
            
            if (pessoa == null) {
                return null;
            }

            System.out.println();
            if (pessoa.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nenhuma pessoa cadastrada");
                return null;
            }
            String iden = null;
           cmbCoordenador.removeAllItems();
           cmbCoordenador.addItem(" - - Selecione - -");
            // percorre lista e mostra todas as contas
            for (Pessoa p : pessoa) {
                cmbCoordenador.addItem(p.getPes_nome());
                if(p.getPes_nome().equals(nome)){
                    iden = p.getPes_iden();
                }
                if(p.getPes_iden().equals(nome)){
                    cmbCoordenador.removeAllItems();
                    iden = p.getPes_nome();
                }
            }
        
        return iden; 
    }

